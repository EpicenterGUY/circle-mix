'use strict';
const fs=require('fs');
const path=require('path');
const file=path.join(__dirname,'apply-mouse-aim-feel.js');
let src=fs.readFileSync(file,'utf8').replace(/\r\n/g,'\n');
const before='\\`${mode} must immediately display a 90 degree jump\\`';
const after='mode+" must immediately display a 90 degree jump"';
if(src.includes(before)) src=src.replace(before,after);
else if(!src.includes(after)) throw new Error('Unable to repair generated mouse aim assertion message.');
fs.writeFileSync(file,src);
console.log('Patched mouse aim transform template.');
